// Car object
const car = {
    make: "TOYOTA HILUX",
    model: "CAMRY",
    year: 2021
};

// Function to print car details
function printCarDetails(car){
    console.log("Make:", car.make);
    console.log("Model:", car.model);
    console.log("Year:", car.year);
}

// Call the function with the car object
printCarDetails(car);