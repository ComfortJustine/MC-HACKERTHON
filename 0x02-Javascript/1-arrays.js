let favoriteFruits = ["apple", "banana", "mango", "orange", "watermelon"];
console.log("Original array:", favoriteFruits);

// Add a new fruit to the beginning
favoriteFruits.unshift("pineapple");
console.log("After adding a new fruit to the beginning:", favoriteFruits);

// Remove a fruit from the end
favoriteFruits.pop();
console.log("After removing a fruit from the end:", favoriteFruits);